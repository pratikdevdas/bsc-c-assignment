/* Using relational operators */
#include <stdio.h>
int main()
{
int x, y;

 x = 10;
 y = 25;
 printf("Given x = %d, y = %d, \n", x, y);
 printf("x >= y produces: %d\n", x >= y);
 printf("x == y produces: %d\n", x == y);
 printf("x < y produces: %d\n", x < y);
 printf("y > x produces: %d\n", y > x);
 printf("x != y - 18 produces: %d\n", x != y - 18);
 printf("x + y != 35 produces: %d\n", x + y != 35);
 return 0;
 }
