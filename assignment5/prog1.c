#include <stdio.h>
int main()
{
int a, b;
 a = 40;
 b = 60;

printf("The value of a&b = %d\n",a&b);
printf("The value of a|b = %d\n",a|b);
printf("The value of a^b = %d\n",a^b);
printf("The value of ~a = %d\n",~a);
printf("The value of a<<2 = %d\n",a<<2);
printf("The value of a>>2 = %d\n",a>>2);

 return 0;
}
